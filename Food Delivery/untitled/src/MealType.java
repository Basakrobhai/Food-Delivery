/**
 * Enum representing the type of meal.
 *
 * Used to classify food items based on their ingredients:
 * MEAT, VEGETARIAN, or VEGAN.
 *
 * Author: Reyan
 * Version: 1.0
 */
public enum MealType
{
    /** Contains meat or meat-based products */
    MEAT,

    /** Contains no meat but may include animal byproducts (e.g., cheese) */
    VEGETARIAN,

    /** Completely plant-based, no animal products at all */
    VEGAN
}
